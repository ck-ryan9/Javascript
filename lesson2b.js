// Triple equal sign --- Javascript

// Logical operators are used to evaluate 2 or more conditions and also give a boolean answer
// Logical AND(&&) It evaluates to true if and only if all conditions are true

console.log((number1 < number2) && (number2 > number1) && (number3 > number2))

// Logical OR (||) - It evaluates to true if one of the conditions is true
console.log((number1 > number2) || (number2 > number1))

// Logical NOT (!) - It is used to negate a statement
console.log(!(number1 < number2))






